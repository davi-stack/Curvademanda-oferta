package Classes;

public class Produto {
 private String nome;
 private int quantidadeVendida;
 private int quantidadeDisponivevel;
 private float preco;
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public int getQuantidadeVendida() {
	return quantidadeVendida;
}
public void setQuantidadeVendida(int quantidadeVendida) {
	this.quantidadeVendida = quantidadeVendida;
}
public int getQuantidadeDisponivevel() {
	return quantidadeDisponivevel;
}
public void setQuantidadeDisponivevel(int quantidadeDisponivevel) {
	this.quantidadeDisponivevel = quantidadeDisponivevel;
}
public float getPreco() {
	return preco;
}
public void setPreco(float preco) {
	this.preco = preco;
}
 
}
